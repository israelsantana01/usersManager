    private void btnUpdateMouseClicked(java.awt.event.MouseEvent evt)//GEN-FIRST:event_btnUpdateMouseClicked
    {//GEN-HEADEREND:event_btnUpdateMouseClicked
        DBOperations operations = new DBOperations();
        Students studentAltered = new Students();
        studentAltered.setId(this.id); 
        studentAltered.setUsername(txtUsername.getText());
        studentAltered.setSubject(txtSubject.getText());    
        studentAltered.setYear_of_entry(Integer.parseInt(txtYear.getText()));    
        
        
        if (operations.Update(studentAltered)) {
            JOptionPane.showMessageDialog(this, "Item atualizado com sucesso!");
            dispose();
        } else {
            JOptionPane.showMessageDialog(this, "ERRO ao atualizar");
            dispose();
        }   
    }//GEN-LAST:event_btnUpdateMouseClicked

